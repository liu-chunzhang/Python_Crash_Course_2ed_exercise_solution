animalnames = [ 'cat' , 'dog' , 'fish' ]

# 4.2(0)的要求：打印所有的动物名称。
for animalname in animalnames:
	print(animalname)

# 4.2(1)的要求：修改这个程序，使其针对每种动物都打印一个句子。
for animal in animalnames:
	print(f"A {animal} would make a great pet.")

# 4.2(2)的要求：添加总结性句子。
print( "Any of these animals would make a great pet!" )