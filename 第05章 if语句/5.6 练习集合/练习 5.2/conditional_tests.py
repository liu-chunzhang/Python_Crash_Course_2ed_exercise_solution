# 练习5.2整体依然非常繁琐，各分要求依然只举一个例子。
	# 5.2(1)的要求：检查两个字符串相等或不等。
print(f"'audi' == 'audi'? {'audi' == 'audi'}")
print(f"'audi' == 'subaru'? {'audi' == 'subaru'}")

	# 5.2(2)的要求：使用方法lower()的测试。
print(f"'audi' == 'Audi.lower()'? {'audi' == 'Audi'.lower()}")

	# 5.2(3)的要求：涉及相等、不等、大于、小于、大于等于和小于等于的数值测试。
print(f" 3 == 5 ? { 3 == 5 }")
print(f" 3 != 5 ? { 3 != 5 }")
print(f" 3 > 5 ? { 3 > 5 }")
print(f" 3 < 5 ? { 3 < 5 }")
print(f" 3 >= 5 ? { 3 >= 5 }")
print(f" 3 <= 5 ? { 3 <= 5 }")

	# 5.2(4)的要求：使用关键字and和or的测试。
print(f" 3 > 5 and 7 > 5 ? { 3 > 5 and 7 > 5 }")
print(f" 3 > 5 or 7 > 5 ? { 3 > 5 or 7 > 5 }")

	# 5.2(5)的要求：测试特定的值是否包含在列表中。
print(f" 6 in range(1,10)? { 6 in range(1,10) }")

	# 5.2(6)的要求：测试特定的值是否未包含在列表中。
print(f" 6 not in range(1,10)? { 6 not in range(1,10) }")